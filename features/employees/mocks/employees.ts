// ─── Helper ───────────────────────────────────────────────────────────────────

function getDept(id: string) {
  return DEPARTMENTS.find((d) => d.id === id)!;
}

function getPos(id: string) {
  return POSITIONS.find((p) => p.id === id)!;
}

function makeActivity(
  id: string,
  type: Employee['activities'][0]['type'],
  description: string,
  daysAgo: number,
  performedBy: string
) {
  const date = new Date();
  date.setDate(date.getDate() - daysAgo);
  return { id, type, description, timestamp: date.toISOString(), performedBy };
}

